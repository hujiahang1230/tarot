    /**
     * 生成预言者风格的完整解读
     */
    generateOracleReading(card, reversed, position = 'single') {
        const oracleReading = this.oracleCardReadings[card.id];
        const positionCtx = this.oraclePositionContexts[position];

        const opening = this.selectRandom(this.oracleOpenings);
        const closing = this.selectRandom(this.philosophicalClosings);
        const positionIntro = this.selectRandom(positionCtx.intro);
        const prophecy = this.selectRandom(oracleReading.prophecy);
        const destiny = this.selectRandom(oracleReading.destiny);
        const philosophy = this.selectRandom(oracleReading.philosophy);
        const healing = this.selectRandom(oracleReading.healing);
        const transition = this.selectRandom(this.propheticTransitions);

        let reversedText = '';
        if (reversed) {
            reversedText = `\n\n【逆位的神谕】\n\n${this.selectRandom(oracleReading.reversed)}`;
        }

        const orientation = reversed ? '逆位' : '正位';

        return {
            title: `${positionCtx.title}：${card.name} ${orientation}`,
            content: `${opening}\n\n【${positionCtx.title}】\n\n${positionIntro}\n\n此刻出现在你面前的，是${card.name}的${orientation}。\n\n${prophecy}\n\n${transition}\n\n${destiny}\n\n${transition}\n\n${philosophy}\n\n${transition}\n\n${healing}${reversedText}\n\n${closing}`,
            card: card,
            reversed: reversed,
            position: position,
            shortMeaning: reversed ? card.reversed : card.upright
        };
    },

    /**
     * 生成预言者风格的仪式引导
     */
    generateRitualGuidance() {
        return this.selectRandom(this.ritualInvocations);
    },

    /**
     * 选择随机项
     */
    selectRandom(arr) {
        return arr[Math.floor(Math.random() * arr.length)];
    }

    /**
     * Analyze user question to determine theme and emotional tone
     */
    analyzeQuestion(question) {
        if (!question || question.trim() === '') {
            return { theme: 'general', emotion: 'neutral', keywords: [] };
        }

        const q = question.toLowerCase();
        let bestTheme = 'general';
        let maxMatches = 0;

        for (const [theme, data] of Object.entries(this.questionThemes)) {
            if (theme === 'general') continue;
            const matches = data.keywords.filter(kw => q.includes(kw)).length;
            if (matches > maxMatches) {
                maxMatches = matches;
                bestTheme = theme;
            }
        }

        // Detect emotional tone
        let emotion = 'neutral';
        const positiveWords = ['希望', '期待', '开心', '幸福', '美好', '顺利', '成功'];
        const negativeWords = ['痛苦', '难过', '伤心', '迷茫', '害怕', '担心', '焦虑', '绝望', '困惑'];
        const questionWords = ['为什么', '怎么办', '如何', '会不会', '能不能', '该不该'];

        const posCount = positiveWords.filter(w => q.includes(w)).length;
        const negCount = negativeWords.filter(w => q.includes(w)).length;
        const quesCount = questionWords.filter(w => q.includes(w)).length;

        if (negCount > posCount) emotion = 'seeking_comfort';
        else if (quesCount > 0) emotion = 'seeking_guidance';
        else if (posCount > 0) emotion = 'hopeful';
        else emotion = 'contemplative';

        return { theme: bestTheme, emotion, keywords: question.split(/[\s,，.。!！?？]+/).filter(w => w.length > 1) };
    },


    /**
     * Generate compatibility analysis for any pair
     */
    analyze(sign1Id, sign2Id) {
        const sign1 = ZodiacData.getSign(sign1Id);
        const sign2 = ZodiacData.getSign(sign2Id);

        if (!sign1 || !sign2) return null;

        // Check if we have specific pair analysis
        const pairKey1 = `${sign1Id}-${sign2Id}`;
        const pairKey2 = `${sign2Id}-${sign1Id}`;
        const pairData = this.pairSoulAnalysis[pairKey1] || this.pairSoulAnalysis[pairKey2];

        if (pairData) {
            return this.formatSoulAnalysis(sign1, sign2, pairData);
        }

        // Generate dynamic analysis based on elements
        return this.generateDynamicSoulAnalysis(sign1, sign2);
    },


    /**
     * Generate a complete AI-style reading for a single card
     */
    generateReading(card, reversed, position = 'single') {
        const reading = this.cardReadings[card.id];
        const positionCtx = this.positionContexts[position];

        // Select random passages
        const opening = this.selectRandom(this.openings);
        const closing = this.selectRandom(this.closings);
        const positionIntro = this.selectRandom(positionCtx.intro);
        const cosmic = this.selectRandom(reading.cosmic);
        const emotional = this.selectRandom(reading.emotional);
        const philosophical = this.selectRandom(reading.philosophical);
        const healing = this.selectRandom(reading.healing);
        const transition = this.selectRandom(this.transitions);

        let reversedText = '';
        if (reversed) {
            reversedText = `

【逆位的深意】

${this.selectRandom(reading.reversed)}`;
        }

        const orientation = reversed ? '逆位' : '正位';

        return {
            title: `${positionCtx.title}：${card.name} ${orientation}`,
            content: `${opening}

【${positionCtx.title}】

${positionIntro}

此刻出现在你面前的，是${card.name}的${orientation}。

${cosmic}

${transition}

${emotional}

${transition}

${philosophical}

${transition}

${healing}${reversedText}

${closing}`,
            card: card,
            reversed: reversed,
            position: position,
            shortMeaning: reversed ? card.reversed : card.upright
        };
    },

    /**
     * Generate reading for multiple cards
     */
    generateMultiReading(cards, spreadType) {
        const config = SpreadConfig[spreadType];
        const opening = this.selectRandom(this.openings);
        const closing = this.selectRandom(this.closings);

        let overallIntro = `${opening}

此刻，宇宙为你展开了${config.name}的牌阵。每一张牌都是拼图中的一块，共同构成了一幅关于你生命的画卷。

`;

        let readings = [];
        let synthesis = '';

        cards.forEach((cardData, index) => {
            const position = config.positions[index];
            const reading = this.generateReading(cardData.card, cardData.reversed, position);
            readings.push(reading);
        });

        // Generate synthesis
        synthesis = this.generateSynthesis(readings, config);

        return {
            intro: overallIntro,
            readings: readings,
            synthesis: synthesis,
            closing: closing,
            fullText: `${overallIntro}${readings.map(r => r.content).join('\n\n---\n\n')}\n\n---\n\n【综合解读】\n\n${synthesis}\n\n${closing}`
        };
    },

